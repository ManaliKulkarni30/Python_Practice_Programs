def Start():
    print("Jay Ganesh")
    print("Jay Ganesh")
    print("Jay Ganesh")
    print("Jay Ganesh")
    print("Jay Ganesh")

def StartI():
    iCnt = 0
    while iCnt < 5:
        print("Jay Ganesh")
        iCnt = iCnt + 1

def main():
    print("Output by sequence")
    Start()
    print("Output by iteration")
    StartI()

if __name__ == "__main__":
    main()
